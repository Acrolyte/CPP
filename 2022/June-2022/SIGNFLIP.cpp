void solve(){
	ll n,k,x; cin>>n>>k;
	vl v1,v2;
	ll t = n;
	TC(t){
		cin>>x;
		if(x < 0) v1.pb(x);
		else v2.pb(x);
	}

	ll sum = 0,c=0;
	SORT(v1);
	for(auto i: v1){
		if(c<k) {
			sum += i*-1;
		}
		c++;
	}
	for(auto i: v2){
		sum += i;
	}

	cout<<sum<<'\n';
}

